function delopt(selectCtrl, mTxt) {
    if (selectCtrl.options) {
        for (i = selectCtrl.options.length; i >= 0; i--) {
            selectCtrl.options[i] = null;
        }
    }
    selectCtrl.options[0] = new Option(mTxt, "")
    selectCtrl.selectedIndex = 0;
}

